package Inheritance;

class Person {
        String name;
        int contactNum; 
            
        void setName(String n){
            this.name = n;
        }
        String getName()    {
            return name;
        }
        void setContactNum(int c){
            this.contactNum = c;
        }
        
        int getContactNum(){
            return contactNum;
        }
        
    }    